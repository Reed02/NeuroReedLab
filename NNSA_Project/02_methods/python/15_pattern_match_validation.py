# ==========================
# Compare only row 1 with itself, then matched ones against all other rows
# Track all datasets with at least one match across the matrix
# ==========================

def percentage_match(y1, y2):
    matches = 0
    for v1, v2 in zip(y1, y2):
        if v1 == 0:
            if v2 == 0:
                matches += 1
            continue
        deviation = abs(v1 - v2) / abs(v1)
        if deviation <= 0.15:  # 15% deviation = 85% match
            matches += 1
    return matches / len(y1) * 100

# Track all datasets with at least one match (dataset identifier: (row_index, col_index))
datasets_with_matches = set()

# --------------------------
# Step 1: Compare datasets in Row 1
# --------------------------
row1 = matrix_5x5[0]
print("--- Comparing datasets in Row 1 ---")
for i in range(len(row1)):
    for j in range(i+1, len(row1)):
        _, y1 = row1[i]
        _, y2 = row1[j]
        match_percent = percentage_match(y1, y2)
        if match_percent >= 60:
            result = "MATCH"
            datasets_with_matches.add((0,i))
            datasets_with_matches.add((0,j))
        else:
            result = "NO MATCH"
        print(f"Dataset {i+1} vs {j+1}: {match_percent:.2f}% -> {result}")

# --------------------------
# Step 2: Compare matched Row 1 datasets against all other rows (Rows 2–5)
# --------------------------
matched_row1_indices = [col for (row, col) in datasets_with_matches if row == 0]

print("\n--- Comparing matched Row 1 datasets against all other rows (Rows 2–5) ---")
for col_index in matched_row1_indices:
    _, y1 = matrix_5x5[0][col_index]
    for row_index in range(1,5):  # Rows 2–5
        row = matrix_5x5[row_index]
        for other_col_index, (_, y2) in enumerate(row):
            match_percent = percentage_match(y1, y2)
            if match_percent >= 60:
                result = "MATCH"
                datasets_with_matches.add((0,col_index))  # Row1 dataset
                datasets_with_matches.add((row_index, other_col_index))  # Other dataset
            else:
                result = "NO MATCH"
            print(f"Row1 Dataset {col_index+1} vs Row{row_index+1} Dataset {other_col_index+1}: "
                  f"{match_percent:.2f}% -> {result}")

# --------------------------
# Final summary
# --------------------------
print("\nAll datasets that had at least one match (row, dataset index):")
for row_index, col_index in sorted(datasets_with_matches):
    print(f"Row {row_index+1}, Dataset {col_index+1}")

# --------------------------
# Plot datasets with at least one match
# --------------------------
plt.figure(figsize=(12,6))
colors = plt.cm.tab20.colors  # color palette for up to 20 datasets

for idx, (row_index, col_index) in enumerate(sorted(datasets_with_matches)):
    x, y = matrix_5x5[row_index][col_index]
    plt.plot(x, y, label=f"Row {row_index+1} Dataset {col_index+1}", color=colors[idx % len(colors)])

plt.xlabel("X")
plt.ylabel("Y")
plt.title("Datasets with at least one match")
plt.legend()
plt.grid(True)
plt.show()
